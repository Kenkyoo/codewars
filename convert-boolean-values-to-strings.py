def bool_to_word(boolean):
    if boolean == True:
        return "Yes"
    else:
        return "No"
        
bool_to_word(False)

# La función bool_to_word toma un valor booleano como argumento. Si el valor es True, devuelve la cadena "Yes". Si el valor es False, devuelve la cadena "No". En este caso, al llamar a bool_to_word(False), la función devolverá "No".